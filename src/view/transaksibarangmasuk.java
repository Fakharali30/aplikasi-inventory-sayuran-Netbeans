    public void hasilkode() {
        try {
            String sq = "SELECT * FROM supplier ORDER BY namasp ASC";
            String sqa = "SELECT * FROM barang ORDER BY idbarang ASC";
            java.sql.Statement st = conn.createStatement();
            java.sql.Statement sta = conn.createStatement();
            ResultSet has = st.executeQuery(sq);
            ResultSet hasa = sta.executeQuery(sqa);
            while (has.next()) {
                Object[] ob = new Object[4];
                ob[0] = has.getString(2);
                cmbnamasp.addItem(ob[0]);
            }
            while (hasa.next()) {
                Object[] ob = new Object[4];
                ob[0] = hasa.getString(1);
                cmbidbarang.addItem(ob[0]);
            }
            has.close();
            hasa.close();
            st.close();

        } catch (Exception a) {
        }
    }
    
      public void autonomor() {
        try {
            String sql = "select * from transaksimasuk ORDER by idtransaksi desc ";
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            txtidtransaksi.setText("TRB-0001");

            if (rs.next()) {
                String nofak = rs.getString("idtransaksi").substring(4);
                String AN = "" + (Integer.parseInt(nofak) + 1);
                String nol = "";

                if (AN.length() == 1) {
                    nol = "000";
                } else if (AN.length() == 2) {
                    nol = "00";
                } else if (AN.length() == 3) {
                    nol = "0";
                } else if (AN.length() == 4) {
                    nol = "";
                }
                txtidtransaksi.setText("TRB-" + nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

