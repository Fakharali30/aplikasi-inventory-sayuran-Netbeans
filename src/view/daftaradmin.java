     public void kdan() {
        String kde = "";
        if (cmbjabatan.getSelectedItem() == "Manager") {
            kde = "MGR";
        } else if (cmbjabatan.getSelectedItem() == "Operator") {
            kde = "OPR";
        } else if (cmbjabatan.getSelectedItem() == "Staff") {
            kde = "STF";
        }
        kdpetugas.setText(kde);
    }

     
    public void autonomor() {
        try {
            String sql = "select * from admin where jabatan LIKE '" 
                    + cmbjabatan.getSelectedItem() + "'ORDER by id desc ";
            Statement stat = conn.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            nopetugas.setText("0001");

            if (rs.next()) {
                String nofak = rs.getString("id").substring(3);
                String AN = "" + (Integer.parseInt(nofak) + 1);
                String nol = "";

                if (AN.length() == 1) {
                    nol = "000";
                } else if (AN.length() == 2) {
                    nol = "00";
                } else if (AN.length() == 3) {
                    nol = "0";
                } else if (AN.length() == 4) {
                    nol = "";
                }
                nopetugas.setText("" + nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

