    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String sql = "insert into admin values(?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, kdpetugas.getText() + nopetugas.getText());
            stat.setString(2, txtnamapetugas.getText());
            stat.setString(3, cmbjabatan.getSelectedItem().toString());
            stat.setString(4, txtpassword.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");

            txtnamapetugas.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Data Gagal Disimpan" + e);
        }
        kosong();
        tabeldata();
        
    }//GEN-LAST:event_btnsimpanActionPerformed

